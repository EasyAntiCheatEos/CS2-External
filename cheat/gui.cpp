void gui::BeginRender() noexcept
{
	MSG message;
	while (PeekMessage(&message, 0, 0, 0, PM_REMOVE))
	{
		TranslateMessage(&message);
		DispatchMessage(&message);

		if (message.message == WM_QUIT)
		{
			isRunning = !isRunning;
			return;
		}
	}

	// Start the Dear ImGui frame
	ImGui_ImplDX9_NewFrame();
	ImGui_ImplWin32_NewFrame();
	ImGui::NewFrame();
}
void gui::EndRender() noexcept
{
	ImGui::EndFrame();

	device->SetRenderState(D3DRS_ZENABLE, FALSE);
	device->SetRenderState(D3DRS_ALPHABLENDENABLE, FALSE);
	device->SetRenderState(D3DRS_SCISSORTESTENABLE, FALSE);

	device->Clear(0, 0, D3DCLEAR_TARGET | D3DCLEAR_ZBUFFER, D3DCOLOR_RGBA(0, 0, 0, 255), 1.0f, 0);

	if (device->BeginScene() >= 0)
	{
		ImGui::Render();
		ImGui_ImplDX9_RenderDrawData(ImGui::GetDrawData());
		device->EndScene();
	}

	const auto result = device->Present(0, 0, 0, 0);

	// Handle loss of D3D9 device
	if (result == D3DERR_DEVICELOST && device->TestCooperativeLevel() == D3DERR_DEVICENOTRESET)
		ResetDevice();
}
void gui::windowthread() noexcept
{
	while (true)
	{
		if (GetAsyncKeyState(0x2D) & 0x8000) 
		{
			config::showwindow = !config::showwindow;
			std::this_thread::sleep_for(std::chrono::milliseconds(25)); 
		}

		if (config::showwindow)
		{
			::ShowWindow(gui::window, SW_SHOW);
		}
		else
		{
			config::currenttab = 0;
			::ShowWindow(gui::window, SW_HIDE);
		}

		std::this_thread::sleep_for(std::chrono::milliseconds(25)); 
	}
}
void ShowEntityTable()
{
	if (ImGui::BeginTable("Entity Table", 7, ImGuiTableFlags_Borders | ImGuiTableFlags_RowBg | ImGuiTableFlags_Sortable))
	{
		// Set up headers
		ImGui::TableSetupColumn("Controller", ImGuiTableColumnFlags_WidthStretch);
		ImGui::TableSetupColumn("Pawn", ImGuiTableColumnFlags_WidthStretch);
		ImGui::TableSetupColumn("Name", ImGuiTableColumnFlags_WidthStretch);
		ImGui::TableSetupColumn("Position", ImGuiTableColumnFlags_WidthStretch);
		ImGui::TableSetupColumn("Team ID", ImGuiTableColumnFlags_WidthStretch);
		ImGui::TableSetupColumn("Health", ImGuiTableColumnFlags_WidthStretch);
		ImGui::TableSetupColumn("Steam ID", ImGuiTableColumnFlags_WidthStretch);
		ImGui::TableHeadersRow();

		static int selectedRow = -1; // Track the selected row
		bool rowSelected = false;    // Flag to check if any row is selected

	

		// Populate the table with cached entity data
		for (int i = 0; i < SDK::entities->List.size(); ++i)
		{
			const auto& entity = SDK::entities->List[i];

			ImGui::TableNextRow();

			// Entity Controller
			ImGui::TableNextColumn();
			ImGui::Text("0x%016llX", entity.entityController);

			// Entity Pawn
			ImGui::TableNextColumn();
			ImGui::Text("0x%016llX", entity.entityPawn);

			// Entity Name
			ImGui::TableNextColumn();
			ImGui::TextUnformatted(entity.entityName.c_str());

			// Entity Position
			ImGui::TableNextColumn();
			ImGui::Text("X: %.2f Y: %.2f Z: %.2f", entity.Pos.x, entity.Pos.y, entity.Pos.z);

			// Team ID
			ImGui::TableNextColumn();
			ImGui::Text("%d", entity.teamiddata);

			// Health
			ImGui::TableNextColumn();
			ImGui::Text("%d", entity.HEALTH);

			// Steam ID
			ImGui::TableNextColumn();
			if (entity.STEAMID == 0)
			{
				ImGui::Text("NULL");
			}
			else
			{
				ImGui::Text("%lld", entity.STEAMID);
			}

		}

		ImGui::EndTable();
	}
}

